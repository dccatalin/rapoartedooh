def get_language():
    """Get chosen language from session state or settings"""
    if 'language' not in st.session_state:
        from src.data.company_settings import CompanySettings
        cs = CompanySettings()
        settings = cs.get_settings()
        st.session_state.language = settings.get('language', 'ro')
    return st.session_state.language

def _(text):
    """Translate text based on current language"""
    lang = get_language()
    # If using English, most keys are already in English
    if lang == 'en':
        return TRANSLATIONS['en'].get(text, text)
    return TRANSLATIONS['ro'].get(text, text)

def remove_diacritics(text):
    """
    Removes diacritics from the given text string.
    Useful for PDF generation where fonts might not support all UTF-8 chars.
    """
    if not isinstance(text, str):
        return text
    
    import unicodedata
    # Normalize to NFD form (decomposes characters)
    nfkd_form = unicodedata.normalize('NFD', text)
    # Filter out non-spacing mark characters
    return "".join([c for c in nfkd_form if not unicodedata.combining(c)])
